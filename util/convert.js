// Función para convertir libras a kilogramos
export function librasAKilogramos(libras) 
{
    const kilogramos = libras * 0.453592; // 1 libra = 0.453592 kilogramos
    return kilogramos;
}
  
  // Función para convertir toneladas a kilogramos
export function toneladasAKilogramos(toneladas) 
{
    const kilogramos = toneladas * 1000; // 1 tonelada = 1000 kilogramos
    return kilogramos;
}